weertype = input("Welk weer is het vandaag? Je kan kiezen tussen regen, zon, sneeuw of wind. ")

if weertype == "zon":
    print("De zon schijnt vandaag. Neem je zonnebril mee.")
elif weertype == "regen":
    print("Het regent vandaag. Neem je paraplu mee.")
elif weertype == "sneeuw":
    print("Een wit landschap. Pak je handschoenen maar!")
elif weertype == "wind":
    print("Winderig weertje. Een windbreaker is voldoende.")
else:
    print("Geniet van je dag :-).")

print("Einde programma")
