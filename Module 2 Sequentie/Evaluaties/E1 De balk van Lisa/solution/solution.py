# Vraag de gebruiker om de afmetingen van de balk
lengte = float(input("Geef de lengte van de balk: "))
breedte = float(input("Geef de breedte van de balk: "))
hoogte = float(input("Geef de hoogte van de balk: "))

# Bereken de inhoud van de balk
inhoud = lengte * breedte * hoogte

# Toon de inhoud van de balk
print(f"De inhoud van de balk is {inhoud} kubieke meters.")
